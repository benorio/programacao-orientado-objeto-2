    public void listarTabela() {
        DefaultTableModel tableModel = (DefaultTableModel) jTableConta.getModel();
        tableModel.setNumRows(0);
        DaoConta cdao = new DaoConta();

        for (Conta c : cdao.listar()) {
            tableModel.addRow(new Object[]{
                c.getId(),
                c.getDescricao(),
                c.getValor(),
                c.getData(),
                c.getTipoMovimentacao()
            });
        }
    }
   
