    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // TODO add your handling code here:

        if (jTableConta.getSelectedRow() != -1) {

            Conta c = new Conta();
            DaoConta daoconta = new DaoConta();

            c.setDescricao(jTextFieldDescricao.getText());
            c.setValor(Double.parseDouble(jTextFieldValor.getText()));
            c.setData(jFormattedTextFieldData.getText());
            c.setTipoMovimentacao((String) jComboBoxMovimentacao.getSelectedItem());
            c.setId((int) jTableConta.getValueAt(jTableConta.getSelectedRow(), 0));

            daoconta.alterar(c);

            jTextFieldDescricao.setText("");
            jTextFieldValor.setText("");
            jFormattedTextFieldData.setText("");
            listarTabela();
            somarValores();
            totalReceitas();
            totalDespesas();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha para alterar");
        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jTableContaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableContaMouseClicked
        // TODO add your handling code here:

        if (jTableConta.getSelectedRow() != -1) {
            jTextFieldDescricao.setText(jTableConta.getValueAt(jTableConta.getSelectedRow(), 1).toString());
            jTextFieldValor.setText(jTableConta.getValueAt(jTableConta.getSelectedRow(), 2).toString());
            jFormattedTextFieldData.setText(jTableConta.getValueAt(jTableConta.getSelectedRow(), 3).toString());
        } else {
            JOptionPane.showMessageDialog(null, "Linha não selecionada");
        }
    }//GEN-LAST:event_jTableContaMouseClicked

